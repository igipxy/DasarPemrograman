public class arrayNumbers10 {
    public static void main(String[] args) {


        int[] num = {5, 12, 7, 20};


        for (int i = 0; i < 4; i++) {
            System.out.println(num[i]);
        }

      
        for (int i = 0; i < num.length; i++) {
            System.out.println(num[i]);
        }

        
    }
}